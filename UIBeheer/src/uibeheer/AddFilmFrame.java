    private void Save() {
        String s = ""; //string voor acteurs
        String error = ""; //string voor de error
        
        Film f = new Film();
        if(!txtTitel.getText().isEmpty()){
           f.setTitel(txtTitel.getText()); 
        }
        else{
            error += "<html>Verplicht een titel in te vullen!<br><html>";
            lblError.setText(error);
        }
        if(cbGenres.getSelectedIndex()>0){
            f.setGenre(cbGenres.getSelectedItem().toString());
        }
        else{
            error += "<html>Verplicht een genre te selecteren!<br><html>";
            lblError.setText(error);
        }
        if(!txtJaar.getText().isEmpty()){
            f.setJaar(Integer.parseInt(txtJaar.getText()));
        }
        else{
            error += "<html>Verplicht een jaar in te vullen!<br><html>";
            lblError.setText(error);
        }
        if(!txtInhoud.getText().isEmpty()){
            f.setOmschrijving(txtInhoud.getText());
        }
        else{
            error += "<html>Verplicht een korte inhoud te geven!<br><html>";
            lblError.setText(error);
        }
        if(cbActeur1.getSelectedIndex()>0) s+= cbActeur1.getSelectedItem().toString();
        else{
            error += "<html>Verplicht tenmiste 1 acteur/actrice te selecteren!<html>";
            lblError.setText(error);
        }
        if(cbActeur2.getSelectedIndex()>0) s+= ", " + cbActeur2.getSelectedItem().toString();
        if(cbActeur3.getSelectedIndex()>0) s+= ", " + cbActeur3.getSelectedItem().toString();
        if(cbActeur4.getSelectedIndex()>0) s+= ", " + cbActeur4.getSelectedItem().toString();
        f.setActeurs(s);
        if(lblError.getText().isEmpty()){
           FilmServices.Save(f); 
        }
        else{
            return;
        }
    }

