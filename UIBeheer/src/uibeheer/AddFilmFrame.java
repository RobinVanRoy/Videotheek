    private void FillCbGenres() {
        List<Genre> list = GenreServices.GetAll();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        model.addElement("-- Select Genre --");
        for(Genre obj:list)
        {
            model.addElement(obj);
        }
        cbGenres.setModel(model);
    }

    private void FillCbActeurs() {
        List<Acteur> list = ActeurServices.GetAll();
        DefaultComboBoxModel model1 = new DefaultComboBoxModel();
        DefaultComboBoxModel model2 = new DefaultComboBoxModel();
        DefaultComboBoxModel model3 = new DefaultComboBoxModel();
        DefaultComboBoxModel model4 = new DefaultComboBoxModel();
        model1.addElement("-- Select Acteur/Actrice --");
        model2.addElement("-- Select Acteur/Actrice --");
        model3.addElement("-- Select Acteur/Actrice --");
        model4.addElement("-- Select Acteur/Actrice --");
        for(Acteur obj:list)
        {
            model1.addElement(obj);
            model2.addElement(obj);
            model3.addElement(obj);
            model4.addElement(obj);
        }
        cbActeur1.setModel(model1);
        cbActeur2.setModel(model2);
        cbActeur3.setModel(model3);
        cbActeur4.setModel(model4);
    }
    
